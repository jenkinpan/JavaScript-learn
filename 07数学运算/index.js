let num1 = 100;

// * Basic Math 数学基础
console.log("*******Basic Math*******");
console.log(num1 + 25);     // ! addition 加
console.log(num1 - 100);    // ! subtraction 减
console.log(num1 * 100);    // ! multiplication 乘
console.log(num1 / 1500);   // ! division 除

console.log("\n*******Additional Mathematical Operations*******");
// * Additional Mathematical Operations
console.log(num1 % 1500);   // ! grab the remainder 取余数
console.log(++num1);        // ! Increment 自增运算符（自身增加1，并赋值给其自身）
console.log(--num1);        // ! Decrement 自减运算符（自身减少1，并赋值给自身）

console.log("\n********Using Math Object*******");
// * using the math object
console.log(Math.PI);   // ! pi
console.log(Math.sqrt(num1));   // ! square root 求平方根